    public void Simpan() {
        if (Nama.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ga boleh kosong dong, isi Nama kamu yaa", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (Alamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ga boleh kosong dong, isi Alamat kamu yaa", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (Telepon.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ga boleh kosong dong, isi Email kamu yaa", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (Email.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ga boleh kosong dong, isi Telepon kamu yaa", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            try (FileOutputStream file = new FileOutputStream("Database.dat", true)) {
                String nama = Nama.getText();
                String alamat = Alamat.getText();
                String telepon = Telepon.getText();
                String email = Email.getText();

                file.write(nama.getBytes());
                file.write(alamat.getBytes());
                file.write(telepon.getBytes());
                file.write(email.getBytes());
                file.close();
                JOptionPane.showMessageDialog(null, "Selamat Data Berhasil Di Simpan!");
            } catch (IOException e) {
                e.printStackTrace();

            }

        }
    }
    

